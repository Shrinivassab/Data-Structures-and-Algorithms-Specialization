/*
- Node
- Next
- Prev
* */
class Node {
    constructor(val) {
        this.val = val;
        this.head = null;
        this.prev = null;
    }
}
/*
* Doubly Linked List
* - Head
* - Tail
* - Length
* */

class DoublyLinkedList {
    constructor() {
        this.head = null;
        this.tail = null;
        this.length = 0;
    }

}

